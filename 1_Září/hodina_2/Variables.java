package hodina_2;

public class Variables {
    public static void main(String[] args) {


        // numeric values
        {
            byte a = 1;         // -128 - 127
            short b = 1;        // -32 768 - 32 767
            int c = 1;          // 2^31 - 2^31 - 1
            long d = 1L;        // 2^63 - 2^63 - 1
        }

        // numeric values with a decimal point
        {
            float e = 1.0f;
            double f = 1.0d;
        }

        // true/false values
        {
            boolean g = true;
            boolean h = false;
        }


        // text and letters
        {
            char i = 'a';
            String j = "abc";
        }
    }
}
